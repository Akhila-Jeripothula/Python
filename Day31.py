def check_even_odd(n):
    if n % 2 == 0:
        return "Even"
    else:
        return "Odd"

print(check_even_odd(7))




#2.largest no:

def largest(a, b):
    if a > b:
        return a
    else:
        return b

print(largest(10, 25))


#3.reverse of string
def reverse_string(s):
    return s[::-1]

